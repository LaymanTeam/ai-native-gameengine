'use client';

/**
 * Library — the games the engine has actually generated (generations/<slug>/ with a built
 * game.html), listed from /api/games. Each card animates a palette-derived preview on hover and
 * opens the real playable build. "New game" / "Refine" route to the Studio chat.
 */
import { useEffect, useMemo, useState } from 'react';
import Link from 'next/link';
import {
  Badge, Box, Button, Container, Group, Loader, Paper, Stack, Text, TextInput, Title,
} from '@mantine/core';
import { MiniSim, type MiniGame } from '@/engine/frontend/components/MiniSim';

interface Game { slug: string; title: string; genre: string; }

const PALETTES: MiniGame[] = [
  { field: '#e7e4db', player: '#5f6b4d', enemies: ['#c2a77f', '#8ea1ab', '#b89aa0'] },
  { field: '#e2e7ea', player: '#4a6470', enemies: ['#9aa7b0', '#cdb89a', '#a9b59c'] },
  { field: '#e6e2ea', player: '#6b5570', enemies: ['#b29bb6', '#9aa7b0', '#c2a77f'] },
  { field: '#ece2d6', player: '#9a5e46', enemies: ['#c98e6e', '#a7a382', '#bf9a8a'] },
  { field: '#1b1d22', player: '#aebf86', enemies: ['#c2a77f', '#8ea1ab', '#b89aa0'] },
];

function paletteFor(slug: string): MiniGame {
  let h = 0;
  for (let i = 0; i < slug.length; i++) h = (h * 31 + slug.charCodeAt(i)) >>> 0;
  return PALETTES[h % PALETTES.length]!;
}

function openGame(slug: string) {
  const w = 980; const h = 660;
  const left = (window.screen.width - w) / 2; const top = (window.screen.height - h) / 2;
  window.open(`/api/games/${slug}`, `play_${slug}`, `width=${w},height=${h},left=${left},top=${top}`);
}

function GameCard({ game }: { game: Game }) {
  const [hover, setHover] = useState(false);
  const palette = useMemo(() => paletteFor(game.slug), [game.slug]);
  return (
    <Paper
      withBorder radius="lg"
      style={{ overflow: 'hidden', cursor: 'pointer', transition: 'transform .2s, box-shadow .2s', transform: hover ? 'translateY(-4px)' : undefined, boxShadow: hover ? '0 24px 60px -34px rgba(38,36,31,0.5)' : undefined }}
      onMouseEnter={() => setHover(true)}
      onMouseLeave={() => setHover(false)}
      onClick={() => openGame(game.slug)}
    >
      <Box style={{ position: 'relative', aspectRatio: '16 / 10', background: 'var(--forge-canvas)' }}>
        <MiniSim game={palette} playing={hover} />
        {hover && (
          <Group gap="xs" justify="center" style={{ position: 'absolute', inset: 0 }}>
            <Button size="xs" color="dark" radius="md" onClick={(e) => { e.stopPropagation(); openGame(game.slug); }}>▶ Play</Button>
            <Button size="xs" variant="default" radius="md" component={Link} href="/" onClick={(e) => e.stopPropagation()}>Refine</Button>
          </Group>
        )}
      </Box>
      <Box p="md">
        <Text fw={500} lineClamp={1}>{game.title}</Text>
        <Text size="sm" c="dimmed" lineClamp={1}>{game.genre || 'generated game'}</Text>
      </Box>
    </Paper>
  );
}

export default function LibraryPage() {
  const [games, setGames] = useState<Game[] | null>(null);
  const [query, setQuery] = useState('');

  useEffect(() => {
    let alive = true;
    fetch('/api/games')
      .then((r) => r.json())
      .then((d: { games: Game[] }) => { if (alive) setGames(d.games ?? []); })
      .catch(() => { if (alive) setGames([]); });
    return () => { alive = false; };
  }, []);

  const filtered = useMemo(() => {
    if (!games) return [];
    const q = query.trim().toLowerCase();
    return games.filter((g) => !q || g.title.toLowerCase().includes(q));
  }, [games, query]);

  return (
    <Container size="xl" py={48}>
      <Group justify="space-between" align="flex-end" mb="xs">
        <Title order={1} fw={500}>Library</Title>
        <Button color="dark" radius="md" component={Link} href="/">＋ New game</Button>
      </Group>
      <Text c="dimmed" mb="xl">
        {games === null ? 'Loading…' : (
          <>
            <Text span fw={500} c="var(--forge-ink)">{games.length} game{games.length === 1 ? '' : 's'}</Text> generated by the engine
          </>
        )}
      </Text>

      {games !== null && games.length > 0 && (
        <Group mb="xl">
          <TextInput ml="auto" w={240} value={query} onChange={(e) => setQuery(e.currentTarget.value)} placeholder="Search your games…" aria-label="Search games" />
        </Group>
      )}

      {games === null ? (
        <Group justify="center" py={80}><Loader color="sage" /></Group>
      ) : (
        <Box style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fill, minmax(256px, 1fr))', gap: 22 }}>
          <Paper
            component={Link} href="/" radius="lg"
            style={{ border: '1px dashed rgba(38,36,31,0.2)', minHeight: 224, display: 'flex', flexDirection: 'column', alignItems: 'center', justifyContent: 'center', gap: 12, color: 'var(--mantine-color-dimmed)', textDecoration: 'none' }}
          >
            <Box w={46} h={46} style={{ borderRadius: '50%', border: '1px solid currentColor', display: 'grid', placeItems: 'center', fontSize: 22, fontWeight: 300 }}>＋</Box>
            <Text size="sm">{games.length === 0 ? 'No games yet — create one' : 'Describe a new game'}</Text>
          </Paper>
          {filtered.map((g) => <GameCard key={g.slug} game={g} />)}
        </Box>
      )}
    </Container>
  );
}
